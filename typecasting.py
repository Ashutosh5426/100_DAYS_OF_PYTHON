# TYPECASTING IN PYTHON
# Some Important function used typecasting- int(), float(), str(), ord(), hex(), oct(), tuple(), set(), list(), dict(), 

# There are two types of typecasting in python-
# 1. Explicit Conversion- Done By User
a = "3";
b = "6";
print(int(a) + int(b));

# 2. Implicit Conversion- Done By Python Interpreter
a = 3;
b = 5.4;
print(a + b); # Here the python interpreter automatically converts int to float to prevent data loss.